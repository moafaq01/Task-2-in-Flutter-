class Book {
  final String id;
  final String title;
  final String author;
  final double price;
  final String imageUrl;
  final String category;
  final String description;
  final double rating;

  Book({
    required this.id,
    required this.title,
    required this.author,
    required this.price,
    required this.imageUrl,
    required this.category,
    required this.description,
    required this.rating,
  });
}
